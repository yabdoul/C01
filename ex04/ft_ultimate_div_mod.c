#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)

{
	int	tmp;
	int	tmp2;

	tmp = *a / *b;
	tmp2 = *a % *b;
	*a = tmp;
	*b = tmp2;
}
